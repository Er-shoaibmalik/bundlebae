    <script>
      let currentIndex = 0; // Start with the first slide
      const slides = document.querySelectorAll("section > div > div > div");
      const totalSlides = slides.length;
    
      function updateSlide() {
        const sliderWidth = document.querySelector("section > div").offsetWidth;
        const slidesContainer = document.querySelector("section > div > div");
        slidesContainer.style.transform = `translateX(-${currentIndex * sliderWidth}px)`;
      }
    
      function moveSlide(direction) {
        currentIndex += direction;
        if (currentIndex < 0) {
          currentIndex = totalSlides - 1; // Loop back to the last slide
        } else if (currentIndex >= totalSlides) {
          currentIndex = 0; // Loop back to the first slide
        }
        updateSlide();
      }
    
      // Auto-slide every 5 seconds
      setInterval(() => {
        moveSlide(1);
      }, 5000);
    
      // Ensure the slider resizes correctly
      window.addEventListener("resize", updateSlide);
    
      // Initial setup
      updateSlide();
    </script>
    

